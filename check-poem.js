function checkComma(words, i)
{
  if(i == 0) { return false }
  const lastWord = words[i-1];
  const currWord = words[i];
  const endCharLastWord = lastWord[lastWord.length - 1];
  if(endCharLastWord == '،' && currWord.length == 1 )
  {
    return [currWord];
  }
  else
  {
    return false;
  }
}

