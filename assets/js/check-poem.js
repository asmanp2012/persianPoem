const sortedWords = Object.keys(baseWord).sort((a, b) => baseWord[b].join('').length - baseWord[a].join('').length);

function findCombinations(inputShape, startIndex = 0) {
  const currentCombination = [];
  while(startIndex < inputShape.length) {
    // console.log(startIndex, currentCombination)
    let addBase = false;
    for (const base of  sortedWords) {
      const length = baseWordShape[base].length;
      const subShape = inputShape.substring(startIndex, startIndex + length);
      if(subShape == baseWordShape[base])
      {
        currentCombination.push(base);
        startIndex += length;
        addBase = true;
        break;
      }
    }
    if(!addBase)
    {
      const subShape = inputShape.substring(startIndex);
      currentCombination.push(subShape);
      break;
    }
  }
  return currentCombination;
}


